import turtle

turtle.setup(600, 600, 80, 80)
turtle.pencolor("yellow")
turtle.pensize(4)
turtle.penup()
turtle.fd(-150)
turtle.pendown()
for i in range(18):
    turtle.fd(300)
    turtle.left(100)
turtle.fd(150)
turtle.right(90)
turtle.pensize(8)
turtle.pencolor("green")
turtle.fd(400)
turtle.penup()
turtle.pensize(6)
turtle.pendown()
# 绘制叶子
turtle.fd(-250)
turtle.seth(45)
turtle.circle(-130, 60)
turtle.seth(-135)
turtle.circle(-130, 60)
turtle.seth(135)
turtle.circle(130, 60)
turtle.seth(-45)
turtle.circle(130, 60)
turtle.done()